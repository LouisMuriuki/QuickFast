let ownerID: string = null;
let packageID: string = null;
export const setownerID = (value: string) => {
  ownerID = value;
};
export const getownerID = () => {
  return ownerID;
};

export const setpackageID = (value: string) => {
  packageID = value;
};
export const getpackageID = () => {
  return packageID;
};
